/**
 * @brief 计算 CRC-8 校验值
 *
 * @param data  待校验数据
 * @param len   数据长度（字节）
 * @return      CRC-8 校验值
 *
 * 初始值 0x66，逐字节查表并异或。
 * 算法：r = table[r ^ data[i]]
 */
uint8_t bus_crc8(const uint8_t* data, uint32_t len)
{
    uint8_t r = 0x66u;
    for (uint32_t i = 0; i < len; i++)
        r = g_crc8_tbl[(uint8_t)(r ^ data[i])];
    return r;
}

/**
 * @brief 计算 CRC-16 校验值
 *
 * @param data  待校验数据
 * @param len   数据长度（字节）
 * @return      CRC-16 校验值
 *
 * 初始值 0x913D。
 * 算法：取当前 CRC 高 8 位异或数据字节作为索引，
 *       查表后与 CRC 左移 8 位异或。
 */
uint16_t bus_crc16(const uint8_t* data, uint32_t len)
{
    uint16_t r = 0x913Du;
    for (uint32_t i = 0; i < len; i++)
    {
        const uint8_t idx = (uint8_t)((r >> 8) ^ data[i]);
        r = (uint16_t)(g_crc16_tbl[idx] ^ (uint16_t)(r << 8));
    }
    return r;
}
